#include "../../push_swap.h"

void	rotate_b(t_node **b)
{
	t_node	*tmp;

	tmp = *b;
	*b = (*b)->next;
	tmp->next = NULL;
	ft_lstadd_back(b, tmp);
	write(1, "rb\n", 3);
}
